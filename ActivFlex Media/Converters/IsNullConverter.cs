using System;
using System.Globalization;
using System.Windows.Data;

namespace ActivFlex.Converters
{
    /// <summary>
    /// Converts an object to a boolean. When the
    /// object is null the value will be true.
    /// </summary>
    [ValueConversion(typeof(object), typeof(bool))]
    public class IsNullConverter : IValueConverter
    {
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            return value == null;
        }

        public object ConvertBack(object value, Type targetType, object parameter, CultureInfo culture)
        {
            throw new InvalidOperationException("Invalid conversion: Bool can not be converted to an Object");
        }
    }
}